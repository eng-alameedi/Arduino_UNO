#include "gpio_uno.h"
// #include <cstdint>

void pin_set_mode(unsigned int pin, pin_mode mode)
{
  DDRB |= (mode << pin);
}

void pin_set_state(unsigned int pin, pin_state state)
{
  if(state == HIGH)
    PORTB |= (HIGH << pin);
  else
    PORTB &= ~(HIGH << pin);
}
